#include "./gui_cli.h"

void cli_d_print_rom_memory(chip_4004 *c){
	printf("------------------------------------------ ROM MEMORY -----------------------------------------\n");
	int i;
	for(i = 0; i < 4096; i++){
		if(i%32 == 0 && i != 0)
			printf("\n");	
		printf("%02X ", c->ROM[i]);
	}
	printf("\n-----------------------------------------------------------------------------------------------\n");
}
